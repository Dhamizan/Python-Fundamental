# For Loop

'''
Dimulai dari:
1. For Kondisi
2. Aksinya
'''

# Contoh dengan Range
print("Pertama:")
for i in range(5):
    print(i)

print("\nKedua:")
var_a = 5
for i in range(var_a):
    print(i)
    
# Contoh dengan String
print("\nKetiga:")
for huruf in "Haloo Kamu":
    print(huruf)
    
print("\nKeempat:")
var_b = "Haloo Kamu"
for huruf in var_b:
    print(huruf)