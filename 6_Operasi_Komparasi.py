# Operasi Komparasi

a = 4
b = 5
c = 4

# > : Lebih dari
hasil = a > b
print(hasil)

# < : Kurang dari
hasil = a < b
print(hasil)

# >= : Lebih Dari Sama Dengan
hasil = a >= b
print(hasil)

# <= : Kurang Dari Sama Dengan
hasil = a <= b
print(hasil)

# == : Sama Dengan
hasil = a == b
print(hasil)

# != : Tidak Sama Dengan
hasil = a != b
print(hasil)

# is : Sama
hasil = a is c
print(hasil)

# is not : Tidak Sama
hasil = a is not c
print(hasil)