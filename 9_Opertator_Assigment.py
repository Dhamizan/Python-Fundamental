# Operator Assigment

# Tambah
a = 5
a += 4
print("Hasil Tambah = ", a)

# Kurang
a -= 2
print("Hasil Kurang = ", a)

# Perkalian
a *= 2
print("Hasil Perkalian = ", a)

# Pembagian
a = 6
a /= 2
print("Hasil Pembagian = ", a)

# Perpangkatan/Eksponen
a = 2
a **= 3
print("Hasil Perpangkatan/Eksponen = ", a)

# Modulus
a = 8
a %= 3
print("Hasil Modulus = ", a)

# Floor Div
a //= 3
print("Hasil Floor Div = ", a)

# Not
# Tidak ada bentuk assigment

# And
a = True
a &= True
print("Hasil And = ", a)

# Or
a = True
a |= False
print("Hasil Or = ", a)

# Xor
a = True
a ^= False
print("Hasil Xor = ", a)